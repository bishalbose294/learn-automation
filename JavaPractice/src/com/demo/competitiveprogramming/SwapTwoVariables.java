package com.demo.competitiveprogramming;

public class SwapTwoVariables {

	public static void main(String[] args) {
		
		int a = 5;
		int b = 7;
		System.out.println(a);
		System.out.println(b);
		int c;
		c = a;
		a = b;
		b = c;
		
		System.out.println(a);
		System.out.println(b);
	}

}
